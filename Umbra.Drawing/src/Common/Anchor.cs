using System;

namespace Umbra.Drawing;

/// <summary>
/// Represents the anchor of an <see cref="Element"/> relative to its parent.
/// </summary>
[Flags]
public enum Anchor
{
    Top     = 1 << 0,
    Bottom = 1 << 1,
    Middle = 1 << 2,
    Center = 1 << 3,
    Left = 1 << 4,
    Right = 1 << 5,
}
